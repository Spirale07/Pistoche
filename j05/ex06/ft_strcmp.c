int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (s1[i] == s2[b] || s1[i] != '\0' || s2[b] != '\0')
	{
		i++;
		b++;
	}
	return (s1[i] - s2[b]);
}
